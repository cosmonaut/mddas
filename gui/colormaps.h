class MDDASColorMap : public QwtLinearColorMap
{
public:
    MDDASColorMap(const QColor &from, const QColor &to, const uint mode = 0)
        : QwtLinearColorMap(from, to) {
        _mode = mode;
    }

    QRgb rgb(const QwtInterval &interval, double value) const {
        if (_mode == 1) {
            return QwtLinearColorMap::rgb(QwtInterval(std::log(interval.minValue()),
                                                      std::log(interval.maxValue())),
                                          std::log(value));
        } else {
            return QwtLinearColorMap::rgb(QwtInterval(interval.minValue(),
                                                      interval.maxValue()),
                                          value);
        }

        
    }

    void setLog() {
        _mode = 1;
    }

    void setLinear() {
        _mode = 0;
    }

private:
    /* 0 linear, 1 log */
    uint _mode;
};


